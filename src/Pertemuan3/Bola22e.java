public class Bola22e {
    public double jariBola22E;
    
    public Bola22e(int jrb){
        jariBola22E=jrb;
    }
    
    public double hitungVolumeBola(){
        return jariBola22E*jariBola22E*jariBola22E*22*4/3/7;
    }
    
    public double hitungLuasBola(){
        return jariBola22E*jariBola22E*4*22/7;
    } 
}
